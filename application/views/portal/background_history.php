                    <h3 class="blog-post-title">Background History</h3>

                    <p>The journey of establishment of the Khwaja Yunus Ali University started when the most benevolent
                        industrialist of Bangladesh, Dr. M. M. Amjad Hussain was determined to establish a university in
                        his native village Enayetpur, Sirajgonj in the northwestern part of Bangladesh in fulfilment of
                        the Prophecy of Hazrat Khwaja Yunus Ali Enapetpur (R) . </p>

                    <p>In the meantime, Khwaja Yunus Ali Medical College and Khwaja Yunus Ali Medical College & Hospital
                        were already functioning successfully. After discussing with the members of the Board of
                        Trustees of Khwaja Yunus Ali University Trust, he decided to found the university in the
                        vicinity of the Khwaja Yunus Ali Medical College & Hospital. </p>

                    <p> The official permission of Khwaja Yunus Ali University was obtained from the Ministry of
                        Education on November 12, 2012. </p>

